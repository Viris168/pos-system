    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         try {
            // Get input values
            String username = jTextField1.getText().trim();
            String password = jTextField2.getText().trim();
            String usertype = jComboBox1.getSelectedItem().toString();
            
            // Validate input
            if (username.isEmpty() || password.isEmpty()) {
                JOptionPane.showMessageDialog(this, 
                    "Please enter both username and password", 
                    "Input Required", 
                    JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            // Attempt login
            if (Operation.isLogin(username, password, usertype, this)) {
                JOptionPane.showMessageDialog(this, 
                    "Login successful! Welcome " + LoginSession.Nickname, 
                    "Success", 
                    JOptionPane.INFORMATION_MESSAGE);
                
                        
                     
                         
                            Admin_main admin = new Admin_main();
                            admin.setVisible(true);
                            this.dispose();
                       
                        
            } else {
                JOptionPane.showMessageDialog(this, 
                    "Invalid username, password, or user type", 
                    "Login Failed", 
                    JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error: " + e.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

